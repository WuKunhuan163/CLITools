def run_mcp_login(email=None, recovery_code=None, as_json=False):
    """Generate MCP login workflow.

    Returns exit code: 0 = success (workflow printed)
    """
    workflow = build_login_workflow(email=email, recovery_code=recovery_code)

    if as_json:
        print(json.dumps(workflow, indent=2))
    else:
        print(f"{BOLD}{BLUE}Google Login MCP Workflow{RESET}")
        print(f"  Login URL: {GOOGLE_LOGIN_URL}")
        if email:
            print(f"  Account: {email}")
        print()
        print(f"  {BOLD}Prerequisites:{RESET}")
        print(f"  1. Have your Google account email ready.")
        print(f"  2. Have your account password ready.")
        print(f"  3. Have at least one recovery code ready.")
        print()
        print(f"  {BOLD}How to get recovery codes:{RESET}")
        print(f"  Visit: {RECOVERY_CODES_URL}")
        print(f"  (Sign in > 2-Step Verification > Backup codes > Show codes)")
        print()
        print(f"  Use {BOLD}GOOGLE --mcp-login --json{RESET} for structured workflow instructions.")
    return 0
