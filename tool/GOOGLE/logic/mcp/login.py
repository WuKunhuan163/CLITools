GOOGLE_LOGIN_URL = "https://accounts.google.com/signin"
RECOVERY_CODES_URL = "https://myaccount.google.com/signinoptions/two-step-verification"


def build_recovery_codes_tutorial():
    """Return instructions for obtaining Google recovery codes."""
    return {
        "title": "How to Get Google Recovery Codes",
        "url": RECOVERY_CODES_URL,
        "steps": [
            "Open https://myaccount.google.com/signinoptions/two-step-verification in your regular browser.",
            "Sign in to your Google account if prompted.",
            "Scroll down to the 'Backup codes' section.",
            "Click 'Show codes' (or 'Set up' if not configured yet).",
            "Copy or save the 10 backup codes displayed.",
            "Each code is 8 digits and can only be used once.",
            "Store these codes securely (e.g., password manager).",
            "You can regenerate codes at any time (invalidates old ones).",
        ],
        "note": "Recovery codes bypass phone-based 2FA, enabling login in "
                "environments where phone verification is not possible (like the IDE browser).",
    }


