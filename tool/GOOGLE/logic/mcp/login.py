"""Google Login MCP workflow for the built-in browser.

Provides a structured workflow that guides the AI agent to assist the user
in signing into their Google account via the MCP browser, using backup/recovery
codes for 2FA. This avoids phone-based verification that can't be automated.

Recovery codes are pre-generated by the user from their Google Account settings.
"""
import json
import sys
from pathlib import Path


def _find_project_root():
    curr = Path(__file__).resolve().parent
    while curr != curr.parent:
        if (curr / "tool.json").exists() and (curr / "bin" / "TOOL").exists():
            return curr
        curr = curr.parent
    return Path(__file__).resolve().parent.parent.parent.parent.parent

_project_root = _find_project_root()
if str(_project_root) not in sys.path:
    sys.path.insert(0, str(_project_root))

from logic.interface.config import get_color

BOLD = get_color("BOLD")
GREEN = get_color("GREEN")
